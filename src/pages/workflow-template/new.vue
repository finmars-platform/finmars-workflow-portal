<template>
	<div class="new-workflow-page">
		<div class="pb-2">
			<FmBreadcrumbs :crumbs="crumbs" @update-crumbs="handleCrumbs"/>
		</div>
		<h1>Create New Workflow</h1>

		<!-- Workflow Creation Form -->
		<form class="workflow-form">
			<div class="form-group">
				<label for="name">Workflow Name</label>
				<FmTextField
					v-model="name"
					:rules="[rules.required]"
					label="Daily"
					outlined
					clearable
				/>
			</div>

			<div class="form-group">
				<label for="user_code">User Code</label>
				<FmTextField
					v-model="userCode"
					:rules="[rules.required]"
					label="com.finmars.local:daily"
					outlined
					clearable
				/>
			</div>

			<FmButton type="primary" rounded @click="createWorkflow">Create</FmButton>
		</form>
	</div>
</template>

<script setup>
import {FmBreadcrumbs, FmButton, FmTextField} from "@finmars/ui";

const router = useRouter();
let store = useStore();

let name = ref('');
let userCode = ref('');

const crumbs = ref([
	{title: 'Workflow template', path: 'workflow-template'},
	{title: 'Create New Workflow', path: 'new'}
]);

const rules = {
	required: value => value ? '' : 'Field is required'
}

const handleCrumbs = (newCrumbs, newPath) => {
	router.push(`/${store.realm_code}/${store.space_code}/w` + newPath);
};

// Function to create the new workflow
async function createWorkflow() {
	try {
		// Call your API to create the workflow
		const response = await useApi('workflowTemplate.post', {
			body: {
				name: name.value,
				user_code: userCode.value,
			},
			notifyError: false
		});

		if (response._$error) {
			useNotify({
				type: 'error',
				title: 'Error',
				text: response._$error.error?.details?.errors?.[0]?.detail || 'Failed to create the Workflow Template.'
			});

			throw response._$error;
		}

		// Redirect to the detail edit page after creation
		const createdWorkflowId = response.id;
		router.push(`/${store.realm_code}/${store.space_code}/w/workflow-template/${createdWorkflowId}/`);
	} catch (error) {
		console.error('Error creating workflow:', error);
		// Handle error, e.g., show a notification to the user
	}
}
</script>

